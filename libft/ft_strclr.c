#include "libft.h"

void	ft_strclr(char *s)
{
	size_t		i;
	size_t		len;

	i = 0;
	if (s == NULL)
		return ;
	len = ft_strlen(s);
	while (i < len)
	{
		s[i] = '\0';
		i++;
	}
}
